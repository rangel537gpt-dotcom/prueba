    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String nomArticulo = txtNombreArticulo.getText();
        if (nomArticulo.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "DEBE INGRESAR UN ARTICULO",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtCantidad.getText().isEmpty() || txtTotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "DEBE LLENAR LOS CAMPOS",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (this.tipoCarga == 1) {
            NotaDetalle ncd = (NotaDetalle) this.detalle;
            int cantidad = Integer.valueOf(txtCantidad.getText());
            double total = Double.valueOf(txtTotal.getText());
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this,
                        "LA CANTIDAD DEBE SER MAYOR A CERO",
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (total <= 0) {
                JOptionPane.showMessageDialog(this,
                        "EL PRECIO DEBE SER MAYOR A CERO",
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (ncd.getDocumentoPagoDet().getCantidad() < cantidad) {
                JOptionPane.showMessageDialog(this,
                        "LA CANTIDAD NO PUEDE SER MAYOR A " + ncd.getDocumentoPagoDet().getCantidad(),
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            ncd.setCantidad(cantidad);
            double valorVenta = 0;
            double igv = 0;
            if (ncd.getDocumentoPagoDet().getConceptoPagoDetalle().getTieneIgv().equals("S")) {
                total = Math.round(total * Math.pow(10, 2)) / Math.pow(10, 2);
                valorVenta = total / ((ncd.getDocumentoPagoDet().getConceptoPagoDetalle().getPorcentajeIgv() + 100) / 100);
                valorVenta = Math.round(valorVenta * Math.pow(10, 2)) / Math.pow(10, 2);
                igv = total - valorVenta;
                igv = Math.round(igv * Math.pow(10, 2)) / Math.pow(10, 2);
                ncd.setValorVenta(valorVenta);
                ncd.setIgv(igv);
            } else {
                total = Math.round(total * Math.pow(10, 2)) / Math.pow(10, 2);
                ncd.setValorVenta(total);
                ncd.setIgv(0);
            }
            ncd.setMontoDetraccion(0.0);
            if (!Objects.isNull(ncd.getDocumentoPagoDet().getPorcentajeDetraccion())) {
                if (ncd.getDocumentoPagoDet().getPorcentajeDetraccion() > 0) {
                    ncd.setMontoDetraccion((!Objects.isNull(ncd.getDocumentoPagoDet().getPorcentajeDetraccion()) ? ncd.getDocumentoPagoDet().getPorcentajeDetraccion() : 0.0) * (ncd.getValorVenta() + ncd.getIgv()));
                }
            }
            ((frmNotaDetalle) this.frm).ModificarDetalle(ncd);
        }
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

