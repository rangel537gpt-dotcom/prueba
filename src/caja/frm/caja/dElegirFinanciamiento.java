    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        frmPrincipal fPrin = (frmPrincipal) this.getParent();
        Thread queryThread = new Thread() {
            public void run() {
                //frmPrincipal f = (frmPrincipal) this.getParent();
                dispose();
                frmCargando fCargando = new frmCargando();
                fPrin.AgregarFormulario(fCargando);
                int x = (fPrin.getWidth() - fCargando.getWidth()) / 2;
                int y = (fPrin.getHeight() - fCargando.getHeight()) / 2;
                fCargando.setLocation(x, y);
                fCargando.setVisible(true);
                fCargando.toFront();
                String tipoCliente = "";
                if (rbContador.isSelected()) {
                    tipoCliente = "C";
                } else {
                    tipoCliente = "S";
                }
                fPrin.AbrirFormularioFinanciamiento(tipoCliente);
                fCargando.dispose();
            }
        };
        queryThread.start();
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void btnAceptarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnAceptarKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            rbSociedad.requestFocus();
            rbSociedad.setSelected(true);
        }
    }//GEN-LAST:event_btnAceptarKeyPressed

    private void rbContadorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rbContadorKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            this.btnAceptarActionPerformed(null);
        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            rbSociedad.requestFocus();
            rbSociedad.setSelected(true);
        }
    }//GEN-LAST:event_rbContadorKeyPressed

    private void rbSociedadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_rbSociedadKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            this.btnAceptarActionPerformed(null);
        }
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            rbContador.requestFocus();
            rbContador.setSelected(true);
        }
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            btnAceptar.requestFocus();
        }
    }//GEN-LAST:event_rbSociedadKeyPressed

