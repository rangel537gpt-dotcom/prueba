    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int nroDocumento = Integer.valueOf(txtNroDoc.getText());
        String tipoDocumento = "";
        if (cbTipoComprobante.getSelectedItem().toString().equals("FACTURA")) {
            tipoDocumento = "01";
        }
        if (cbTipoComprobante.getSelectedItem().toString().equals("R. INGRESOS")) {
            tipoDocumento = "02";
        }
        if (cbTipoComprobante.getSelectedItem().toString().equals("BOLETA")) {
            tipoDocumento = "03";
        }
        String documento = "PAGO: " + tipoDocumento + " " + cbSerie.getSelectedItem().toString() + "-" + String.format("%015d", nroDocumento);
        frmPrincipal fPrin = (frmPrincipal) this.getParent();
        fPrin.AgregarGlosa(documento);
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

