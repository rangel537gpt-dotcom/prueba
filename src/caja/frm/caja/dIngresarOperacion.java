    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        try {
            DocumentoPagoBO dBO = DocumentoPagoBO.getInstance();
//                String nroOperacion = txtNroOperacion.getText().trim();
//                String nroCuenta = cbCuenta.getSelectedItem().toString();
            List<Operacion> l = new ArrayList();
            DefaultTableModel m = (DefaultTableModel) tOperacion.getModel();
            for (int i = 0; i < m.getRowCount(); i++) {
                String fechaOperacion = tOperacion.getValueAt(i, 0).toString();
                String nroCuenta = tOperacion.getValueAt(i, 1).toString();
                String nroOperacion = tOperacion.getValueAt(i, 2).toString();
                Operacion operacion = dBO.verificarExisteNroOperacion(nroCuenta, nroOperacion);
                if (operacion != null) {
                    JOptionPane.showMessageDialog(this,
                            "LA OPERACION YA ESTA REGISTRADA CON EL COMPROBANTE " + operacion.getDocumentoPago().getTipoDocSerie().getTipoDocPago().getNombreDocPago() + " " + operacion.getDocumentoPago().getTipoDocSerie().getSerie().getSerie() + "-" + operacion.getDocumentoPago().getNroDocumentoPago() + "\n NOMBRE CLIENTE:" + operacion.getDocumentoPago().getNombreCliente(),
                            "ERROR",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
                Operacion nuevaOperacion = new Operacion();
                nuevaOperacion.setFechaOperacion(f.parse(fechaOperacion));
                nuevaOperacion.setNroCuenta(nroCuenta);
                nuevaOperacion.setNroOperacion(nroOperacion);
                l.add(nuevaOperacion);
            }
            this.fComprobante.agregarNroOperacionFecha(l);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this,
                    "OCURRIO UN ERROR CON LA FECHA",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

