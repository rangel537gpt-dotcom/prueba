    public void CargarDatos(frmComprobantePagoCabecera f, DocumentoPago dp) {
        this.fComprobante = f;
        this.documentoPago = dp;
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        if (dp.getFechaOperacion() != null) {
            txtFecha.setText(format.format(dp.getFechaOperacion()));
        }
        if (dp.getNroOperacion() != null) {
            txtNroOperacion.setText(dp.getNroOperacion());
        }
    }


