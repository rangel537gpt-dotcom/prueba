    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        txtNroOperacion.setText(txtNroOperacion.getText().trim());
        if (!txtNroOperacion.getText().isEmpty() && !txtFecha.getText().isEmpty()) {
            for (int i = 0; i < tOperacion.getRowCount(); i++) {
                String nroCuenta = tOperacion.getValueAt(i, 1).toString();
                String nroOperacion = tOperacion.getValueAt(i, 2).toString();
                if (cbCuenta.getSelectedItem().toString().equals(nroCuenta) && txtNroOperacion.getText().equals(nroOperacion)) {
                    JOptionPane.showMessageDialog(this,
                            "EL NRO DE OPERACION YA EXISTE",
                            "ERROR",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            DefaultTableModel modelDetalle = (DefaultTableModel) tOperacion.getModel();
            int numFilas = modelDetalle.getRowCount();
            modelDetalle.setNumRows(numFilas);
            modelDetalle.addRow(new Object[numFilas]);
//        SimpleDateFormat f = new SimpleDateFormat();
            tOperacion.setValueAt(txtFecha.getText(), numFilas, 0);
            tOperacion.setValueAt(cbCuenta.getSelectedItem().toString(), numFilas, 1);
            tOperacion.setValueAt(txtNroOperacion.getText(), numFilas, 2);
        } else {
            JOptionPane.showMessageDialog(this,
                    "DEBE LLENAR EL NRO DE OPERACION",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tOperacionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tOperacionKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            int fila = tOperacion.getSelectedRow();
            if (fila > -1) {
                DefaultTableModel m = (DefaultTableModel) tOperacion.getModel();
                m.removeRow(fila);
            }
        }
    }//GEN-LAST:event_tOperacionKeyPressed

