    private boolean VerificarCierreDiario(DocumentoPagoBO dpBO) {
        try {
            boolean estaCerrado = dpBO.EstaCerradoElDia(txtFechaDocumento.getText());
            return estaCerrado;
        } catch (Exception e) {
            e.printStackTrace();
            return true;
        }
    }


    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (txtDiasProrroga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "DEBE PONER UN NÚMERO DE DIAS",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtNroDocumento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "DEBE PONER UN NÚMERO DE DOCUMENTO",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        int nroDocumento = Integer.valueOf(txtNroDocumento.getText());

        DocumentoPagoBO dpBO = DocumentoPagoBO.getInstance();
        boolean estaCerradoElDia = this.VerificarCierreDiario(dpBO);
        if (estaCerradoElDia) {
            JOptionPane.showMessageDialog(this,
                    "EL DÍA " + txtFechaDocumento.getText() + " ESTÁ CERRADO",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

//        DocumentoPagoBO dpBO = DocumentoPagoBO.getInstance();
        List<TipoDocPago> listaTipoDocPago = dpBO.ObtenerTodosTipoDocPago();
        TipoDocPago tipoDocumento = null;
        for (TipoDocPago td : listaTipoDocPago) {
            if (cbTipoDoc.getSelectedItem().toString().equals(td.getNombreDocPago())) {
                tipoDocumento = td;
                break;
            }
        }

        boolean existeNroCMP = dpBO.VerificarNroComprobanteExiste(tipoDocumento.getIdTipoDocPago(), nroDocumento, cbSerie.getSelectedItem().toString());
        if (existeNroCMP) {
            JOptionPane.showMessageDialog(this,
                    "EL NRO DE COMPROBANTE DE PAGO YA EXISTE",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        frmPrincipal fPrin = (frmPrincipal) this.getParent();
        fPrin.LlenarDatosFinanciamiento(tipoDocumento.getIdTipoDocPago(), cbSerie.getSelectedItem().toString(), Integer.valueOf(txtDiasProrroga.getText()), nroDocumento, txtFechaDocumento.getText());
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

