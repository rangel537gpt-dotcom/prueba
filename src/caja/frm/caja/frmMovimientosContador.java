    private void buscarContador() {
        txtNombre.setEditable(false);
        txtNumero.setEditable(false);
        btnMovimientosContador.setEnabled(false);
        int tipoBusqueda = 0;
        if (cbFiltro.getSelectedItem() == "POR NOMBRE") {
            tipoBusqueda = 4;
        } else if (cbFiltro.getSelectedItem() == "POR CODIGO") {
            tipoBusqueda = 2;
        }
        ContadorBO doc = ContadorBO.getInstance();
        List<Cliente> l = doc.BuscarContador(txtBusqueda.getText(), tipoBusqueda);
        DefaultTableModel modelo = (DefaultTableModel) tReporteContador.getModel();
        modelo = (DefaultTableModel) tReporteContador.getModel();
        modelo.setRowCount(l.size());
        int contador = 0;
        for (Cliente docPago : l) {
            modelo.setNumRows(contador);
            modelo.addRow(new Object[contador]);
            tReporteContador.setValueAt(docPago.getIdCliente(), contador, 0);
            tReporteContador.setValueAt(contador + 1, contador, 1);
            tReporteContador.setValueAt(docPago.getPapePat() + ' ' + docPago.getPapeMat() + ' ' + docPago.getPnombre(), contador, 2);
            tReporteContador.setValueAt(docPago.getCcodigoCole(), contador, 3);
            contador++;
        }
    }

    public void ImprimirMovimientosContador() {
        try {
            if (this.cliente != null) {
                String jdbcDriver = "com.microsoft.sqlserver.jdbc.SQLServerDriver";
                Class.forName(jdbcDriver);
                SeguridadBO sBO = SeguridadBO.getInstance();
                String url = sBO.getUrlReporte();
                String user = "sa";
                String pass = "4dm1n1str4c10nGOB90570";
                Connection con = DriverManager.getConnection(url, user, pass);
                String fullPath = "";
                Map param;
                fullPath = "reportes/Historial_Contador.jasper";
                param = new HashMap();
                param.put("IDCLIENTE", this.cliente.getIdCliente());
                param.put("Codigo_Colegiatura", this.cliente.getCcodigoCole());
                param.put("Nombre_contador", txtNombre.getText());
                param.put("REPORT_LOCALE", new Locale("en", "EN"));
                JasperPrint JPrint = JasperFillManager.fillReport(fullPath, param, con);
                JasperViewer.viewReport(JPrint, false);
            } else {
                JOptionPane.showMessageDialog(this, "DEBE ELEGIR UN CONTADOR","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }


