    private void tReporteContadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tReporteContadorMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = tReporteContador.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tReporteContador.getModel();
            int idContador = Integer.valueOf(model.getValueAt(fila, 0).toString());
            SeguridadBO sBO = SeguridadBO.getINSTANCE();
            Cliente cli = (Cliente) sBO.CargarObjeto("Cliente", idContador);
            cliente = cli;
            txtNombre.setText(cli.getPapePat() + " " + cli.getPapeMat() + " " + cli.getPnombre());
            txtNumero.setText(cli.getCcodigoCole());
            txtNombre.setEditable(true);
            txtNumero.setEditable(true);
            btnMovimientosContador.setEnabled(true);
        }
    }//GEN-LAST:event_tReporteContadorMouseClicked


