    private void tReporteSociedadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tReporteSociedadesMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = tReporteSociedades.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tReporteSociedades.getModel();
            int idCliente = Integer.valueOf(model.getValueAt(fila, 0).toString());
            SeguridadBO sBO = SeguridadBO.getINSTANCE();
            Cliente cli = (Cliente) sBO.CargarObjeto("Cliente", idCliente);
            this.cliente = cli;
            txtNombreSociedad.setText(cli.getSnombreSociedad());
            txtNumero.setText(cli.getScodigoSoc());
            txtNombreSociedad.setEditable(true);
            txtNumero.setEditable(true);
            btnMovimientosSociedad.setEnabled(true);
        }
    }//GEN-LAST:event_tReporteSociedadesMouseClicked


