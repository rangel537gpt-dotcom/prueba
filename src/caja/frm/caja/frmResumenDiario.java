    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (!chkFecha.isSelected() && !chkComprobante.isSelected()) {
            JOptionPane.showMessageDialog(this,
                    "DEBE ELEGIR UN FILTRO PARA REALIZAR LA BUSQUEDA",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        DocumentoPagoBO dpBO = DocumentoPagoBO.getInstance();
        List<ResumenDiario> lNotas = new ArrayList();
        String filtro = "";
        if (chkFecha.isSelected()) {
            try {
                SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                Date fromDate = df.parse(txtDesde.getText() + " 00:00:00");
                Date toDate = df.parse(txtHasta.getText() + " 23:59:59");
                lNotas = dpBO.BuscarResumenDiario_SegunFecha(fromDate, toDate);
            } catch (ParseException ex) {
                Logger.getLogger(frmResumenDiario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (chkComprobante.isSelected()) {
            if (txtNro.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "DEBE INGRESAR UN NRO DE SOLICITUD",
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            int nro = Integer.valueOf(txtNro.getText());
            filtro = "n.nro = " + nro;
            lNotas = dpBO.BuscarResumenDiario_SegunNroNota(nro);
        }
        DefaultTableModel modelDetalle = (DefaultTableModel) tComprobantes.getModel();
        int contador = 0;
        modelDetalle.setRowCount(0);
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        for (ResumenDiario nc : lNotas) {
            modelDetalle.setNumRows(contador);
            modelDetalle.addRow(new Object[contador]);
            tComprobantes.setValueAt(nc, contador, 0);
            tComprobantes.setValueAt(contador + 1, contador, 1);
            tComprobantes.setValueAt(String.format("%05d", nc.getNro()), contador, 2);
            tComprobantes.setValueAt(f.format(nc.getFecha()), contador, 3);
            tComprobantes.setValueAt(nc.getObservacion(), contador, 4);
            contador++;
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void CargarComprobante() {
        int fila = tComprobantes.getSelectedRow();
        ResumenDiario nc = (ResumenDiario) tComprobantes.getValueAt(fila, 0);
        frmPrincipal f = (frmPrincipal) this.getParent().getParent().getParent().getParent().getParent();
        f.AbrirFormularioResumenDiarioDetalle(nc);
    }

    private void tComprobantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tComprobantesMouseClicked
        if (evt.getClickCount() == 2) {
            final frmPrincipal fPrin = (frmPrincipal) this.getParent().getParent().getParent().getParent().getParent();
            Thread queryThread = new Thread() {
                public void run() {
                    frmCargando fCargando = new frmCargando();
                    fPrin.AgregarFormulario(fCargando);
                    int x = (fPrin.getWidth() - fCargando.getWidth()) / 2;
                    int y = (fPrin.getHeight() - fCargando.getHeight()) / 2;
                    fCargando.setLocation(x, y);
                    fCargando.setVisible(true);
                    fCargando.toFront();
                    CargarComprobante();
                    fCargando.dispose();
                }
            };
            queryThread.start();
        }
    }//GEN-LAST:event_tComprobantesMouseClicked

