    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String dateDesde = txtFechaDesde.getText();
        String dateHasta = txtFechaHasta.getText();
        frmPrincipal fPrin = (frmPrincipal) this.getParent();
        if (cbTipoArchivo.getSelectedItem().toString().equals("HALCONT")) {
            fPrin.ExportarArchivosMigracionHalCont(this, dateDesde, dateHasta);
        } else {
            fPrin.ExportarArchivosMigracionNuevoSistemaContable(this, dateDesde, dateHasta);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

