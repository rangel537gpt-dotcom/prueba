    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtDni.getText().isEmpty() && !txtApeMat.getText().isEmpty() && !txtApePat.getText().isEmpty() && !txtNombres.getText().isEmpty()) {
            PersonaBO pBO = PersonaBO.getInstance();
            List l = pBO.BuscarPersona(txtDni.getText(), 1);
            if (l.isEmpty()) {
                Cliente p = new Cliente();
                p.setTipoCliente("P");
                p.setTipoDocIdentidad(new TipoDocIdentidad(1));
                p.setPnroDocumento(txtDni.getText());
                p.setPapePat(txtApePat.getText());
                p.setPapeMat(txtApeMat.getText());
                p.setPnombre(txtNombres.getText());
                pBO.GuardarPersona(p);
                this.f.AgregarPersona(p);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "LA PERSONA YA EXISTE",
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "DEBE LLENAR TODOS LOS CAMPOS",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

