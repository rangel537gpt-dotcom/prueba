    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (this.scotiabankRetorno != null) {
                if (this.scotiabankRetorno.getArchivo() != null) {
                    String[] options = new String[]{"DESCARGAR", "CANCELAR"};
                    int response = JOptionPane.showOptionDialog(null, "OPCIONES", "SELECCIONAR OPCIÓN",
                            JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
                            null, options, options[0]);
                    if (response == 0) {
                        byte contenido[] = this.scotiabankRetorno.getArchivo();
                        if (Desktop.isDesktopSupported()) {
                            if (contenido != null) {
                                FileOutputStream fileOuputStream = new FileOutputStream(this.scotiabankRetorno.getNombreArchivo());
                                fileOuputStream.write(contenido);
                                fileOuputStream.close();
                                File archivo = new File(this.scotiabankRetorno.getNombreArchivo());
                                //fileOuputStream.
                                Desktop.getDesktop().open(archivo);
                            } else {
                                JOptionPane.showMessageDialog(this,
                                        "NO HAY ARCHIVO PDF ASOCIADO A ESTE OPERACIÓN",
                                        "ERROR",
                                        JOptionPane.ERROR_MESSAGE);
                            }
                        }
                    } else {
                        return;
                    }
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex + ""
                    + "\nNo se ha encontrado el archivo",
                    "ADVERTENCIA!!!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

