    public void CargarDatos(Object f, int tc, ScotiabankRetorno br) {
        this.frm = f;
        this.tipoCarga = tc;
        this.scotiabankRetorno = br;
        if (tc == 1) {
            btnSubirArchivo.setEnabled(true);
            btnSeleccionarArchivo.setEnabled(true);
            if (br != null) {
                if (br.getSeSubioArchivo() != null) {
                    if (br.getSeSubioArchivo().equals("S")) {
                        btnSeleccionarArchivo.setEnabled(false);
                        btnSubirArchivo.setEnabled(false);
                    }
                }
            }
        }
        if (tc == 2) {
            btnSeleccionarArchivo.setEnabled(false);
            btnSubirArchivo.setEnabled(false);
        }
    }

    private void CargarArchivo() {
        try {
            JFileChooser file = new JFileChooser();
            file.showOpenDialog(this);
            File abre = file.getSelectedFile();
            if (this.tipoCarga == 1) {
                ((frmScotiabankRetornoSubir) this.frm).CargarArchivo(abre);
                int resp = JOptionPane.showConfirmDialog(null, "¿DESEA SUBIR EL ARCHIVO?", "PREGUNTA", JOptionPane.YES_NO_OPTION);
                if (resp == 0) {
                    ((frmScotiabankRetornoSubir) this.frm).SubirArchivo();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex + ""
                    + "\nNo se ha encontrado el archivo",
                    "ADVERTENCIA!!!", JOptionPane.WARNING_MESSAGE);
        }
    }


    private void btnSeleccionarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarArchivoActionPerformed
        this.CargarArchivo();
        this.dispose();
    }//GEN-LAST:event_btnSeleccionarArchivoActionPerformed

    private void btnSubirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirArchivoActionPerformed
        if (this.tipoCarga == 1) {
            ((frmScotiabankRetornoSubir) this.frm).SubirArchivo();
        }
        this.dispose();
    }//GEN-LAST:event_btnSubirArchivoActionPerformed

    private void btnGenerarComprobantesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarComprobantesActionPerformed
        SeguridadBO sBO = SeguridadBO.getInstance();
        ScotiabankRetorno br = (ScotiabankRetorno) sBO.CargarObjeto("ScotiabankRetorno", this.scotiabankRetorno.getId());
        if (br.getSeGeneroComprobantes()!= null) {
            if (br.getSeGeneroComprobantes().equals("S")) {
                JOptionPane.showMessageDialog(this, "YA SE GENERÓ COMPROBANTES DE PAGO", "ERROR", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "YA SE GENERÓ COMPROBANTES DE PAGO", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.tipoCarga == 1) {
            ((frmScotiabankRetornoSubir) this.frm).GenerarComprobante();
        }
        if (this.tipoCarga == 2) {
            ((frmScotiabankRetorno) this.frm).GenerarComprobante();
        }
        this.dispose();
    }//GEN-LAST:event_btnGenerarComprobantesActionPerformed

