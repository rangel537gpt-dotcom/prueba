    private void tReporteEmpresasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tReporteEmpresasMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = tReporteEmpresas.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tReporteEmpresas.getModel();
            int idCliente = Integer.valueOf(model.getValueAt(fila, 0).toString());
            SeguridadBO sBO = SeguridadBO.getINSTANCE();
            Cliente cli = (Cliente) sBO.CargarObjeto("Cliente", idCliente);
            this.cliente = cli;
            txtNombreEmpresa.setText(cli.getEnombre());
            txtNumero.setText(cli.getEruc());
            txtNombreEmpresa.setEditable(true);
            txtNumero.setEditable(true);
            btnMovimientosEmpresa.setEnabled(true);
        }
    }//GEN-LAST:event_tReporteEmpresasMouseClicked


