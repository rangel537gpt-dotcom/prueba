    private void buscarContador() {

        txtNombreEmpresa.setEditable(false);
        txtNumero.setEditable(false);

        btnMovimientosEmpresa.setEnabled(false);

        int tipoBusqueda = 0;
        if (cbFiltro.getSelectedItem() == "POR RUC") {
            tipoBusqueda = 1;
        } else if (cbFiltro.getSelectedItem() == "POR NOMBRE") {
            tipoBusqueda = 2;
        }

        EmpresaBO e = EmpresaBO.getInstance();
        List<Cliente> l = e.BuscarEmpresa(txtBusqueda.getText(), tipoBusqueda);
        DefaultTableModel modelo = (DefaultTableModel) tReporteEmpresas.getModel();
        modelo = (DefaultTableModel) tReporteEmpresas.getModel();
        modelo.setRowCount(l.size());
        int contador = 0;
        for (Cliente docPago : l) {
            modelo.setNumRows(contador);
            modelo.addRow(new Object[contador]);
            tReporteEmpresas.setValueAt(docPago.getIdCliente(), contador, 0);
            tReporteEmpresas.setValueAt(contador + 1, contador, 1);
            tReporteEmpresas.setValueAt(docPago.getEnombre(), contador, 2);
            tReporteEmpresas.setValueAt(docPago.getEruc(), contador, 3);
            contador++;
        }
    }

    public void ImprimirMovimientosEmpresas() {
        try {
            if (this.cliente != null) {
                String jdbcDriver = "com.microsoft.sqlserver.jdbc.SQLServerDriver";
                Class.forName(jdbcDriver);
                SeguridadBO sBO = SeguridadBO.getInstance();
                String url = sBO.getUrlReporte();
                String user = "sa";
                String pass = "4dm1n1str4c10nGOB90570";
                Connection con = DriverManager.getConnection(url, user, pass);
                String fullPath = "";
                Map param;
                fullPath = "reportes/Historial_Empresas.jasper";
                param = new HashMap();
                String rucEmpresa = "";
//                rucEmpresa = txtEmpresa.getText();
                String nombreEmpresa = "";
                nombreEmpresa = txtNombreEmpresa.getText();
                param.put("IDCLIENTE", this.cliente.getIdCliente());
                param.put("RUC", rucEmpresa);
                param.put("Nombre_Empresa", nombreEmpresa);
                param.put("REPORT_LOCALE", new Locale("en", "EN"));
                JasperPrint JPrint = JasperFillManager.fillReport(fullPath, param, con);
                JasperViewer.viewReport(JPrint, false);
            }
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
    }


