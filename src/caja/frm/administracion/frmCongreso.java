    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed


        if (!txtNombreCongreso.getText().isEmpty() && !txtFechaCongreso.getText().isEmpty()) {
//            if (txtNroOficio.getText().length() != 8) {
//                JOptionPane.showMessageDialog(this,
//                        "El DNI debe tener 8 digitos",
//                        "ERROR",
//                        JOptionPane.ERROR_MESSAGE);
//                return;
//            }
            this.congreso.setNombre(txtNombreCongreso.getText());

            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            try {
                this.congreso.setFecha(format.parse(txtFechaCongreso.getText()));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this,
                        "OCURRIO UN PROBLEMA CON LA FECHA",
                        "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            CongresoBO cBO = CongresoBO.getInstance();
            if (this.congreso.getId() == 0) {
                boolean seGuardo = cBO.GuardarCongreso(this.congreso);
                if (seGuardo) {
                    JOptionPane.showMessageDialog(this,
                            "SE GUARDÓ LOS DATOS",
                            "OK",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "NO SE PUDO GUARDAR LOS DATOS",
                            "ERROR",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } else {
                boolean seGuardo = cBO.ActualizarCongreso(this.congreso);
                if (seGuardo) {
                    JOptionPane.showMessageDialog(this,
                            "SE ACTUALIZÓ LOS DATOS",
                            "OK",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "NO SE PUDO ACTUALIZAR LOS DATOS",
                            "ERROR",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            txtNombreCongreso.setEditable(false);
            txtFechaCongreso.setEditable(false);
            btnFechaOficio.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnFechaOficio.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Por favor llene todos los Campos",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

