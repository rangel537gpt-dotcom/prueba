    private void buscarOficio() {
        CongresoBO oBO = CongresoBO.getInstance();
//        List<Oficio> listado = oBO.BuscarOficio(title, SOMEBITS);
        List<Congreso> listado = oBO.ObtenerTodasCongresos();
        DefaultTableModel modelo = (DefaultTableModel) tOficio.getModel();
        modelo.setRowCount(0);
        int contador = 0;
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        for (Congreso o : listado) {
            modelo.setNumRows(contador);
            modelo.addRow(new Object[contador]);
            tOficio.setValueAt(contador + 1, contador, 0);
            tOficio.setValueAt(o.getNombre(), contador, 1);
            tOficio.setValueAt(format.format(o.getFecha()), contador, 2);
            tOficio.setValueAt(o, contador, 3);
            contador++;
        }
        txtNombreCongreso.setText("");
        txtFechaCongreso.setText("1900-01-01");
        txtNombreCongreso.setEditable(false);
        txtFechaCongreso.setEditable(false);
    }

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        frmPrincipal fPrin = (frmPrincipal) this.getParent().getParent().getParent().getParent().getParent();
        Thread queryThread = new Thread() {
            public void run() {
                frmCargando fCargando = new frmCargando();
                fPrin.AgregarFormulario(fCargando);
                int x = (fPrin.getWidth() - fCargando.getWidth()) / 2;
                int y = (fPrin.getHeight() - fCargando.getHeight()) / 2;
                fCargando.setLocation(x, y);
                fCargando.setVisible(true);
                fCargando.toFront();
                buscarOficio();
                fCargando.dispose();
            }
        };
        queryThread.start();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void tOficioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tOficioMouseClicked
        try {
            if (evt.getClickCount() == 2) {
                int fila = tOficio.getSelectedRow();
                DefaultTableModel model = (DefaultTableModel) tOficio.getModel();
                Congreso c = (Congreso) model.getValueAt(fila, 3);
                this.congreso = c;
                txtNombreCongreso.setText(c.getNombre());
                SimpleDateFormat formatoDeFecha = new SimpleDateFormat("yyyy-MM-dd");
                if (c.getFecha() != null) {
                    txtFechaCongreso.setText(formatoDeFecha.format(c.getFecha()));
                } else {
                    txtFechaCongreso.setText("1900-01-01");
                }
                txtNombreCongreso.setEditable(false);
                txtFechaCongreso.setEditable(false);
                btnFechaOficio.setEnabled(false);
                btnGuardar.setEnabled(false);
                btnEditar.setEnabled(true);
                btnFechaOficio.setEnabled(false);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tOficioMouseClicked

