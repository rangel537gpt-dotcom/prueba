    private void buscarCobrador() {
        txtNombre.setEditable(false);
        String filtro = " u.nombre like '%" + txtNombre.getText().trim() + "%'";
        ManagerBO c = ManagerBO.getInstance();
        List<Universidad> l = c.ObtenerUniversidad_SegunFiltro(filtro);
        DefaultTableModel model = (DefaultTableModel) tUniversidad.getModel();
        model = (DefaultTableModel) tUniversidad.getModel();
        model.setRowCount(l.size());
        int cont = 0;
        for (Universidad p : l) {
            model.setNumRows(cont);
            model.addRow(new Object[cont]);
            tUniversidad.setValueAt(p, cont, 0);
            tUniversidad.setValueAt(cont + 1, cont, 1);
            tUniversidad.setValueAt(p, cont, 2);
            cont++;
        }
    }


    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Thread queryThread = new Thread() {
            public void run() {
                pbCobrador.setIndeterminate(true);
                pbCobrador.setStringPainted(true);
                pbCobrador.setString("BUSCANDO COBRADOR, POR FAVOR ESPERE");
                pbCobrador.setValue(100);
                pbCobrador.repaint();
                buscarCobrador();
                pbCobrador.setString("BUSQUEDA FINALIZADA");
                pbCobrador.setStringPainted(false);
                pbCobrador.setIndeterminate(false);
            }
        };
        queryThread.start();
    }//GEN-LAST:event_btnBuscarActionPerformed

