    private static boolean ValidarFecha(String fecha) {
        try {
            SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
            formatoFecha.setLenient(false);
            formatoFecha.parse(fecha);
        } catch (ParseException e) {
            return false;
        }
        return true;
    }


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtNombre.getText().isEmpty()) {
            try {
                this.universidad.setNombre(txtNombre.getText().trim());
                ManagerBO cob = ManagerBO.getInstance();
                if (this.universidad.getId() == 0) {
                    this.universidad.setBorrado("1");
                    cob.GuardarObjeto(this.universidad);
                    JOptionPane.showMessageDialog(this, "Se guardÃ³ los datos ", "OK",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    cob.ActualizarObjeto(this.universidad);
                    JOptionPane.showMessageDialog(this, "Se actualizÃ³ los datos", "OK",
                            JOptionPane.INFORMATION_MESSAGE);
                }
                txtNombre.setEditable(false);
                txtNombre.setText("");
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Debe llenar todos los Campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tUniversidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tUniversidadMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = tUniversidad.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tUniversidad.getModel();
            this.universidad = (Universidad) model.getValueAt(fila, 0);
            txtNombre.setText(this.universidad.getNombre());
            txtNombre.setEditable(true);
            if (this.accesoGuardar) {
                btnGuardar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tUniversidadMouseClicked

