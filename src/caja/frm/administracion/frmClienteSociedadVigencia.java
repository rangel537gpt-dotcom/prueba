    public void cargarDatos(frmSociedad fr, int tc, int fi, ClienteSociedadVigencia dh) {
        this.fila = fi;
        this.tipoCarga = tc;
        this.frm = fr;
        this.clienteSociedadVigencia = dh;
//        txtFechaAfiliacion.setText("");
        txtFechaVigenciaDesde.setText("");
        txtFechaVigenciaHasta.setText("");
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
//        if (dh.getFechaAfiliacion() != null) {
//            txtFechaAfiliacion.setText(f.format(dh.getFechaAfiliacion()));
//        }
        if (dh.getFechaDesde() != null) {
            txtFechaVigenciaDesde.setText(f.format(dh.getFechaDesde()));
        }
        if (dh.getFechaHasta() != null) {
            txtFechaVigenciaHasta.setText(f.format(dh.getFechaHasta()));
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

//        if (txtFechaAfiliacion.getText().trim().isEmpty()) {
//            JOptionPane.showMessageDialog(this,
//                    "Debe ingresar la fecha de afiliaci√≥n",
//                    "ERROR",
//                    JOptionPane.ERROR_MESSAGE);
//            return;
//        }
        if (txtFechaVigenciaDesde.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar la fecha vigencia desde",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtFechaVigenciaHasta.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar la fecha vigencia hasta",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.clienteSociedadVigencia.setBorrado("1");
        SimpleDateFormat formatoDeFecha = new SimpleDateFormat("yyyy-MM-dd");
        try {
//            this.auditorIndependiente.setFechaAfiliacion(txtFechaAfiliacion.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaAfiliacion.getText()));
            this.clienteSociedadVigencia.setFechaDesde(txtFechaVigenciaDesde.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaVigenciaDesde.getText()));
            this.clienteSociedadVigencia.setFechaHasta(txtFechaVigenciaHasta.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaVigenciaHasta.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error en el formato de fecha, debe tener el siguiente formato: yyyy-MM-dd", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.tipoCarga == 1) {
            this.frm.agregarVigencia(this.clienteSociedadVigencia);
        } else {
            this.frm.actualizarVigencia(this.fila, clienteSociedadVigencia);
        }
//            cBO.ActualizarObjeto(this.derechoHabiente);
//            JOptionPane.showMessageDialog(this, "Se  el derecho habiente", "OK", JOptionPane.WARNING_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

