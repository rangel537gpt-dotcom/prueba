    private void buscarTipoDocumetoIdentidad() {

        txtCodigo.setEditable(false);
        txtNombre.setEditable(false);
        txtDescripcion.setEditable(false);
    
        
        int tipoBusqueda = 0;
        if (cbFiltro.getSelectedItem() == "POR NOMBRE") {
            tipoBusqueda = 1;
        } else if (cbFiltro.getSelectedItem() == "POR CODIGO") {
            tipoBusqueda = 2;
        } else if (cbFiltro.getSelectedItem() == "POR DESCRIPCION") 
        {   tipoBusqueda = 3;
        }
        
        TipoDocIdentidadBO tdi = TipoDocIdentidadBO.getInstance();
        List<TipoDocIdentidad> l = tdi.BuscarTipoDocumntoIdentidad(txtBusqueda.getText(), tipoBusqueda);
        DefaultTableModel modelo = (DefaultTableModel) tIdentidad.getModel();
        modelo = (DefaultTableModel) tIdentidad.getModel();
        modelo.setRowCount(l.size());
        int contador = 0;
        for (TipoDocIdentidad di : l) {
            modelo.setNumRows(contador);
            modelo.addRow(new Object[contador]);
            tIdentidad.setValueAt(di.getIdTipoDocIdentidad(), contador, 0);
            tIdentidad.setValueAt(contador + 1, contador, 1);
            tIdentidad.setValueAt(di.getNombre(), contador, 2);
            //tIdentidad.setValueAt(di.getCodigoDocIde(), contador, 3);
            tIdentidad.setValueAt(di.getDescripcion(), contador, 4);
            contador++;
        }
          
    }
    
    
    private void txtBusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyPressed
        if (evt.getKeyCode() == 10) {
            this.buscarTipoDocumetoIdentidad();
        }
    }//GEN-LAST:event_txtBusquedaKeyPressed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Thread queryThread = new Thread() {
            public void run() 
            {
                
                pbTipoDocIdentidad.setIndeterminate(true);
                pbTipoDocIdentidad.setStringPainted(true);
                pbTipoDocIdentidad.setString("BUSCANDO TIPO DOCUMENTO IDENTIDAD, POR FAVOR ESPERE");
                pbTipoDocIdentidad.setValue(1000);
                pbTipoDocIdentidad.repaint();
                buscarTipoDocumetoIdentidad();
                pbTipoDocIdentidad.setString("BUSQUEDA FINALIZADA");
                pbTipoDocIdentidad.setStringPainted(false);
                pbTipoDocIdentidad.setIndeterminate(false);

            }
        };
        queryThread.start();
    }//GEN-LAST:event_btnBuscarActionPerformed

