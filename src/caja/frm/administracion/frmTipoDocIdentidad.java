    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty())
                //&& !txtDescripcion.getText().isEmpty()) 
                
        {
            int fila = tIdentidad.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tIdentidad.getModel();
            String codigo = String.valueOf(model.getValueAt(fila, 1));
            
            try
            {
                if(tipoDocIdentidad == null){
                    JOptionPane.showMessageDialog(this, "Debes seleccionar un Tipo de documento de Identidad", "OK",
                            JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                
                //tipoDocIdentidad.setCodigoDocIde(txtCodigo.getText());
                tipoDocIdentidad.setNombre(txtNombre.getText());
                tipoDocIdentidad.setDescripcion(txtDescripcion.getText());
                
            
                TipoDocIdentidadBO tipoDocIde = TipoDocIdentidadBO.getInstance();
                tipoDocIde.ActualizarTipoDocumntoIdentidad(tipoDocIdentidad, txtCodigo.getText());
                
                
                txtCodigo.setEditable(false);
                txtNombre.setEditable(false);
                txtDescripcion.setEditable(false);
                

                txtCodigo.setText("");
                txtNombre.setText("");
                txtDescripcion.setText("");
                
            
                JOptionPane.showMessageDialog(this, "Se actualizó el Tipo de Documento de Identidad Correctamente", "OK",
                        JOptionPane.INFORMATION_MESSAGE);
            
            }
            
            catch(Exception e)
            {
                e.printStackTrace();
            }
         

        }
        else{
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el Tipo de Documento de Pago", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

