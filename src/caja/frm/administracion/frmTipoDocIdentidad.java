    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty())
                //&& !txtDescripcion.getText().isEmpty()) 
                
        {
            if (txtCodigo.getText().length() != 2) {
                JOptionPane.showMessageDialog(this,
                        "El Código debe tener 2 digitos", "ERROR",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                TipoDocIdentidad tdi = new TipoDocIdentidad();
                //tdi.setCodigoDocIde(txtCodigo.getText());
                tdi.setNombre(txtNombre.getText());
                tdi.setDescripcion(txtDescripcion.getText());
                
                
                TipoDocIdentidadBO tipoDocIdentidad = TipoDocIdentidadBO.getInstance();
                tipoDocIdentidad.GuardarTipoDocumntoIdentidad(tdi, txtCodigo.getText());
               

                txtCodigo.setEditable(false);
                txtNombre.setEditable(false);
                txtDescripcion.setEditable(false);
                
                
                txtCodigo.setText("");
                txtNombre.setText("");
                txtDescripcion.setText("");
                
                JOptionPane.showMessageDialog(this, "Se insertó el Tipo de Documento de Identidad Correctamente", "OK",
                        JOptionPane.INFORMATION_MESSAGE);

            } 
        catch (Exception e) {
                e.printStackTrace();
            }

        } 
        else {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los Campos", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tIdentidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tIdentidadMouseClicked
        if (evt.getClickCount() == 2) {
            
            btnGuardar.setEnabled(false);
            
            int fila = tIdentidad.getSelectedRow();
            DefaultTableModel model = (DefaultTableModel) tIdentidad.getModel();
            String codigo = String.valueOf(model.getValueAt(fila, 3));
            
            TipoDocIdentidadBO tipoDocIde = TipoDocIdentidadBO.getInstance();
            tipoDocIdentidad = tipoDocIde.ObtenerTipoDocumntoIdentidad(codigo);
            
            
            
            
            //txtCodigo.setText(tipoDocIdentidad.getCodigoDocIde());
            txtNombre.setText(tipoDocIdentidad.getNombre());
            txtDescripcion.setText(tipoDocIdentidad.getDescripcion());
            
            txtCodigo.setEditable(true);
            txtNombre.setEditable(true);
            txtDescripcion.setEditable(true);
            
        }
    }//GEN-LAST:event_tIdentidadMouseClicked

