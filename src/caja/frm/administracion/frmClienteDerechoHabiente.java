    public void cargarDatos(frmContadorDetalle fr, int tc, int fi, ClienteDerechoHabiente dh) {
        this.fila = fi;
        this.tipoCarga = tc;
        this.frm = fr;
        txtFechaNacPariente.setText("");
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        derechoHabiente = dh;
        txtDNI.setText(dh.getDni());
        txtNombrePariente.setText(dh.getNombre());
        txtApellidoPaterno.setText(dh.getApePat());
        txtApellidoMaterno.setText(dh.getApeMat());
        txtCelular.setText(dh.getCelular());
        txtEmail.setText(dh.getEmail());
        if (dh.getParentesco() != null) {
            cbParentesco.setSelectedItem(dh.getParentesco());
        }
        if (dh.getFechaNacimiento() != null) {
            txtFechaNacPariente.setText(f.format(dh.getFechaNacimiento()));
        }
        if (dh.getSexo() != null) {
            if (dh.getSexo().equals("M")) {
                cbSexoParentesco.setSelectedItem("MASCULINO");
            } else {
                if (dh.getSexo().equals("F")) {
                    cbSexoParentesco.setSelectedItem("FEMENINO");
                } else {
                    cbSexoParentesco.setSelectedItem("-");
                }
            }
        } else {
            cbSexoParentesco.setSelectedItem("-");
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtDNI.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar el dni",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtDNI.getText().length() != 8) {
            JOptionPane.showMessageDialog(this,
                    "El DNI debe tener 8 digitos", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtNombrePariente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar el nombre",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtNombrePariente.getText().length() > 30) {
            JOptionPane.showMessageDialog(this,
                    "El nombre no puede tener mas de 30 caracteres", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtApellidoPaterno.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar el apellido paterno",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtApellidoPaterno.getText().length() > 30) {
            JOptionPane.showMessageDialog(this,
                    "El apellido paterno no puede tener mas de 30 caracteres", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtApellidoMaterno.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar el apellido materno",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtApellidoMaterno.getText().length() > 30) {
            JOptionPane.showMessageDialog(this,
                    "El apellido materno no puede tener mas de 30 caracteres", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cbParentesco.getSelectedItem().toString().equals("-")) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar el parentesco",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cbSexoParentesco.getSelectedItem().toString().equals("-")) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar el sexo",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtFechaNacPariente.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar la fecha de nacimiento",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtEmail.getText().length() > 50) {
            JOptionPane.showMessageDialog(this,
                    "El e-mail no puede tener mas de 50 caracteres", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtCelular.getText().length() > 10) {
            JOptionPane.showMessageDialog(this,
                    "El nro de celular no puede tener mas de 10 caracteres", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.derechoHabiente.setNombre(txtNombrePariente.getText().trim().toUpperCase());
        this.derechoHabiente.setParentesco(cbParentesco.getSelectedItem().toString());
        this.derechoHabiente.setDni(txtDNI.getText().trim());
        this.derechoHabiente.setNombre(txtNombrePariente.getText().trim().toUpperCase());
        this.derechoHabiente.setApePat(txtApellidoPaterno.getText().trim().toUpperCase());
        this.derechoHabiente.setApeMat(txtApellidoMaterno.getText().trim().toUpperCase());
        this.derechoHabiente.setCelular(txtCelular.getText().trim());
        this.derechoHabiente.setEmail(txtEmail.getText().trim());
        this.derechoHabiente.setBorrado("1");
        SimpleDateFormat formatoDeFecha = new SimpleDateFormat("yyyy-MM-dd");
        try {
            this.derechoHabiente.setFechaNacimiento(txtFechaNacPariente.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaNacPariente.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error en el formato de fecha, debe tener el siguiente formato: yyyy-MM-dd", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cbSexoParentesco.getSelectedItem() == "MASCULINO") {
            this.derechoHabiente.setSexo("M");
        } else {
            if (cbSexoParentesco.getSelectedItem() == "FEMENINO") {
                this.derechoHabiente.setSexo("F");
            } else {
                this.derechoHabiente.setSexo("-");
            }
        }
        if (this.tipoCarga == 1) {
            this.frm.agregarDerechoHabiente(this.derechoHabiente);
        } else {
            this.frm.actualizarDerechoHabiente(this.fila, derechoHabiente);
        }
//            cBO.ActualizarObjeto(this.derechoHabiente);
//            JOptionPane.showMessageDialog(this, "Se  el derecho habiente", "OK", JOptionPane.WARNING_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

