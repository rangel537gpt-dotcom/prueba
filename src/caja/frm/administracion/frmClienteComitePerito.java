    public void cargarDatos(frmContadorDetalle fr, int tc, int fi, ClienteComitePerito dh) {
        this.fila = fi;
        this.tipoCarga = tc;
        this.frm = fr;
        this.comitePerito = dh;
//        txtFechaAfiliacion.setText("");
        txtFechaDesde.setText("");
        txtFechaHasta.setText("");
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
//        if (dh.getFechaAfiliacion() != null) {
//            txtFechaAfiliacion.setText(f.format(dh.getFechaAfiliacion()));
//        }
        if (dh.getDesde() != null) {
            txtFechaDesde.setText(f.format(dh.getDesde()));
        }
        if (dh.getHasta() != null) {
            txtFechaHasta.setText(f.format(dh.getHasta()));
        }
        if (dh.getTipo() != null) {
            if (dh.getTipo().equals("J")) {
                cbTipo.setSelectedItem("REPEJ");
            } else {
                if (dh.getTipo().equals("F")) {
                    cbTipo.setSelectedItem("REPEF");
                } else {
                    cbTipo.setSelectedItem("-");
                }
            }
        } else {
            cbTipo.setSelectedItem("-");
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (cbTipo.getSelectedItem().equals("-")) {
            JOptionPane.showMessageDialog(this,
                    "Debe elegir un tipo de comite",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtFechaDesde.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar la fecha desde desde",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtFechaHasta.getText().trim().isEmpty() && !cbTipo.getSelectedItem().equals("REPEF")) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar la fecha hasta",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.comitePerito.setBorrado("1");
        SimpleDateFormat formatoDeFecha = new SimpleDateFormat("yyyy-MM-dd");
        try {
//            this.comitePerito.setFechaAfiliacion(txtFechaAfiliacion.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaAfiliacion.getText()));
            this.comitePerito.setDesde(txtFechaDesde.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaDesde.getText()));
            this.comitePerito.setHasta(txtFechaHasta.getText().trim().isEmpty() ? null : formatoDeFecha.parse(txtFechaHasta.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error en el formato de fecha, debe tener el siguiente formato: yyyy-MM-dd", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cbTipo.getSelectedItem() == "REPEJ") {
            this.comitePerito.setTipo("J");
        } else {
            if (cbTipo.getSelectedItem() == "REPEF") {
                this.comitePerito.setTipo("F");
            } else {
                this.comitePerito.setTipo("-");
            }
        }
        if (this.tipoCarga == 1) {
            this.frm.agregarComitePerito(this.comitePerito);
        } else {
            this.frm.actualizarComitePerito(this.fila, comitePerito);
        }
//            cBO.ActualizarObjeto(this.derechoHabiente);
//            JOptionPane.showMessageDialog(this, "Se  el derecho habiente", "OK", JOptionPane.WARNING_MESSAGE);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

